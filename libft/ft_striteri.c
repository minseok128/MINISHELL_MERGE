#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	len;
	unsigned int	i;

	i = 0;
	len = (unsigned int)ft_strlen(s);
	while (i < len)
		f(i++, s++);
}
